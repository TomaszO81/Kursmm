# odwocony string funkcja zwraca

def odwroc(fraza):
    """
    Odwraca podany string
    :param fraza: fraza do odwrocenia
    :return:  zwaracam stringa, odwrocona fraza
    """
    fraza = fraza[::-1]
    return fraza

